"""
    dict1 = {键:值, 键:值...}
    dict2 = {}
    dict3 = dict()

    字典用 大括号 包裹
    冒号左边的叫：键   右边的叫： 值
    键具有唯一性，值可以重复
"""
dict1 = {'杨过':'小龙女','萧炎':'林动','天':'地'}
dict2 = {}
dict3 = dict()

print(f'dict1: {dict1}')
print(f'dict2: {dict2}')
print(f'dict3: {dict3}')

print(type(dict1))
print(type(dict2))
print(type(dict3))